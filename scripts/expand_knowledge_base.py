def main():
    # 读取原有数据
    original = []
    with open(INPUT_CSV, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            original.append(row)
    print(f"原有数据: {len(original)} 条")

    # 生成新数据
    new_entries = []
    for entry in get_templates():
        new_entries.append({
            "module": entry[0],
            "problem_pattern": entry[1],
            "problem_desc": entry[2],
            "root_cause": entry[3],
            "solution": entry[4],
            "tags": entry[5],
            "severity": entry[6],
        })
    print(f"生成新数据: {len(new_entries)} 条")

    # 合并去重（按 problem_desc 去重）
    seen = set()
    merged = []

    # 先加原有数据
    for row in original:
        key = row["problem_desc"]
        if key not in seen:
            seen.add(key)
            merged.append(row)

    # 再加新数据
    for entry in new_entries:
        key = entry["problem_desc"]
        if key not in seen:
            seen.add(key)
            merged.append(entry)

    print(f"合并去重后: {len(merged)} 条")

    # 写入新 CSV
    with open(OUTPUT_CSV, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=[
            "module", "problem_pattern", "problem_desc",
            "root_cause", "solution", "tags", "severity"
        ])
        writer.writeheader()
        writer.writerows(merged)

    print(f"✅ 已写入: {OUTPUT_CSV}")

    # 统计各模块数量
    from collections import Counter
    module_count = Counter(row["module"] for row in merged)
    print("\n各模块分布:")
    for module, count in module_count.most_common():
        print(f"  {module}: {count} 条")

    # 统计问题模式
    pattern_count = Counter(row["problem_pattern"] for row in merged)
    print("\n问题模式分布:")
    for pattern, count in pattern_count.most_common():
        print(f"  {pattern}: {count} 条")


if __name__ == "__main__":
    main()
